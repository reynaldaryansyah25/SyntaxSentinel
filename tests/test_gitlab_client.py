import httpx
import pytest
import respx

from app.core.config import Settings
from app.services.gitlab_mcp_client import (
    GitLabAuthenticationError,
    GitLabMCPClient,
    GitLabNotFoundError,
    GitLabRateLimitError,
)


def make_settings(dry_run: bool = True) -> Settings:
    return Settings(
        _env_file=None,
        gitlab_base_url="https://gitlab.example.com",
        gitlab_personal_access_token="test-token",
        gitlab_webhook_secret="webhook-secret",
        gitlab_project_id=123,
        dry_run=dry_run,
    )


@pytest.mark.asyncio
@respx.mock
async def test_list_pipeline_jobs_and_filter_failed_jobs() -> None:
    route = respx.get(
        "https://gitlab.example.com/api/v4/projects/123/pipelines/456/jobs"
    ).mock(
        return_value=httpx.Response(
            200,
            json=[
                {"id": 1, "status": "success"},
                {"id": 2, "status": "failed"},
            ],
        )
    )

    client = GitLabMCPClient(settings=make_settings())

    jobs = await client.list_pipeline_jobs(123, 456)
    failed_jobs = await client.get_failed_jobs(123, 456)

    assert route.called
    assert len(jobs) == 2
    assert failed_jobs == [{"id": 2, "status": "failed"}]



@pytest.mark.asyncio
async def test_dry_run_write_operations_do_not_call_gitlab() -> None:
    client = GitLabMCPClient(settings=make_settings(dry_run=True))

    branch = await client.create_branch(123, "syntaxsentinel/fix-pipeline-456", "main")
    commit = await client.commit_file_changes(
        123,
        "syntaxsentinel/fix-pipeline-456",
        "Fix pipeline failure",
        [{"action": "update", "file_path": "app.py", "content": "print('ok')"}],
    )
    merge_request = await client.create_merge_request(
        123,
        "syntaxsentinel/fix-pipeline-456",
        "main",
        "Fix pipeline failure",
        "Generated by SyntaxSentinel.",
    )
    note = await client.add_merge_request_note(123, 1, "Safety note")

    assert branch["dry_run"] is True
    assert commit["dry_run"] is True
    assert commit["action_count"] == 1
    assert merge_request["dry_run"] is True
    assert note["dry_run"] is True



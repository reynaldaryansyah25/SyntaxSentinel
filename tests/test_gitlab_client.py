@pytest.mark.asyncio
@pytest.mark.parametrize(
    ("status_code", "exception_type"),
    [
        (401, GitLabAuthenticationError),
        (404, GitLabNotFoundError),
        (429, GitLabRateLimitError),
    ],
)
@respx.mock
async def test_gitlab_error_mapping(status_code: int, exception_type: type[Exception]) -> None:
    respx.get("https://gitlab.example.com/api/v4/projects/123").mock(
        return_value=httpx.Response(status_code, json={"message": "error"})
    )
    client = GitLabMCPClient(settings=make_settings())

    with pytest.raises(exception_type):
        await client.get_project(123)


@pytest.mark.asyncio
@respx.mock
async def test_retries_server_errors() -> None:
    route = respx.get("https://gitlab.example.com/api/v4/projects/123").mock(
        side_effect=[
            httpx.Response(500, json={"message": "temporary"}),
            httpx.Response(200, json={"id": 123}),
        ]
    )
    client = GitLabMCPClient(settings=make_settings(), max_retries=1)

    response = await client.get_project(123)

    assert route.call_count == 2
    assert response == {"id": 123}
